import re
from pathlib import Path

class JavaLLMRepairAgent:
    """
    Repairs common Java code issues generated by LLMs.
    This agent runs before compilation and dramatically
    reduces compilation failures.
    """

    OPTIONAL_IMPORT = "import java.util.Optional;"
    LIST_IMPORT = "import java.util.List;"

    OPTIONAL_PATTERN = re.compile(r"\bOptional<")
    LIST_PATTERN = re.compile(r"\bList<")

    PACKAGE_RE = re.compile(r"^\s*package\s+[a-zA-Z0-9_.]+\s*;")

    def run(self, factory):

        root = factory.output_root / "backend/src/main/java"

        if not root.exists():
            return

        for path in root.rglob("*.java"):

            try:
                content = path.read_text(encoding="utf-8")
            except Exception:
                continue

            original = content

            lines = content.splitlines()

            # -----------------------------
            # Detect package line
            # -----------------------------
            pkg_index = None
            for i, line in enumerate(lines):
                if self.PACKAGE_RE.match(line):
                    pkg_index = i
                    break

            if pkg_index is None:
                continue

            imports = set()
            existing_imports = set()

            for line in lines:
                if line.startswith("import "):
                    existing_imports.add(line.strip())

            # -----------------------------
            # Detect Optional usage
            # -----------------------------
            if self.OPTIONAL_PATTERN.search(content):
                if self.OPTIONAL_IMPORT not in existing_imports:
                    imports.add(self.OPTIONAL_IMPORT)

            # -----------------------------
            # Detect List usage
            # -----------------------------
            if self.LIST_PATTERN.search(content):
                if self.LIST_IMPORT not in existing_imports:
                    imports.add(self.LIST_IMPORT)

            # -----------------------------
            # Inject imports
            # -----------------------------
            if imports:

                insert_index = pkg_index + 1

                for imp in sorted(imports):
                    lines.insert(insert_index, imp)
                    insert_index += 1

                factory.log(f"🔧 Java LLM repair applied: {path.name}")

            fixed = "\n".join(lines)

            if fixed != original:
                try:
                    path.write_text(fixed, encoding="utf-8")
                except Exception:
                    pass